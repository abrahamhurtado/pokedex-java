        hpBar.setValue(stats.getHp());
        hpLabel.setText(String.valueOf(stats.getHp()));
        attackBar.setValue(stats.getAttack());
        attackLabel.setText(String.valueOf(stats.getAttack()));
        defenseBar.setValue(stats.getDefense());
